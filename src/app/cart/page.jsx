"use client"
import { CartContext, cartProductPrice } from "@/components/AppContext"
import SectionHeaders from "@/components/layout/SectionHeaders"
import { useContext, useEffect } from "react"
import AddressInputs from "@/components/layout/AddressInputs";
import { useState } from "react"
import { useProfile } from "@/components/UseProfile"
import toast from "react-hot-toast"
import CartProduct from "@/components/menu/CartProduct"

export default function CartPage() {
    const {cartProducts, removeCartProduct} = useContext(CartContext);
    const {data:profileData} = useProfile();

    const [address, setAddress] = useState([]);

    useEffect(() => {
        if (typeof window !== 'undefined') {
            if (window.location.href.includes('canceled=1')) {
                toast.error('Payment failed')
            }
        }
    }, [])

    useEffect(() => {
        if(profileData?.city) {
            const {phone, city, country, streetAddress, postalCode} = profileData;
            const addressFromProfile = {phone, city, country, streetAddress, postalCode};
            setAddress(addressFromProfile)
        }
    }, [profileData])

    let subtotal = 0;
    for (const product of cartProducts) {
        subtotal += cartProductPrice(product)
    }

    function handleAddressChange(propName, value) {
        setAddress(prevAddress => {
            return ({...prevAddress, [propName]:value});
        })
    }

    async function proceedToCheckout(ev) {
        ev.preventDefault();
        const promise = new Promise(async(resolve, reject) => {
            fetch('/api/checkout', {
                method: 'POST',
                headers: {'Content-Type' : 'application/json'},
                body: JSON.stringify({
                    address,
                    cartProducts,
                }),
            }).then(async (response) => {
                if(response.ok) {
                    resolve();
                    window.location = await response.json();
                } else {
                    reject();
                }
            });
        });

        await toast.promise(promise, {
            loading: 'Preparing your order...',
            success: 'Redirectering to payment',
            error: 'Something went wrong... Please try again later.'
        })
    }

    if(cartProducts?.length === 0) {
        return (
            <section className="mt-8">
                <div className="text-center">
                    <SectionHeaders mainHeader="Cart"/>
                    <p className="mt-4">Your shopping cart is empty.</p>
                </div>
            </section>
        );
    }

    return (
        <section className="mt-8">
            <div className="text-center">
                <SectionHeaders mainHeader="Cart"/>
            </div>
            <div className="mt-8 grid grid-cols-2 gap-8">
                <div>
                    {cartProducts?.length > 0 && cartProducts.map((product, index) => (
                        <CartProduct key={product._id} product={product} onRemove={removeCartProduct} index={index}/>  
                    ))}
                    <div className="py-2 pr-16 flex justify-end items-center">
                        <div className="text-gray-500">
                            Subtotal: <br />
                            Delivery: <br />
                            Total:
                        </div>
                        <div className="font-semibold pl-2 text-right">
                            ${subtotal} <br />
                            $5 <br />
                            ${subtotal + 5} 
                        </div>
                    </div>
                </div>
                <div className="p-4 rounded-lg">
                    <h2>Checkout</h2>
                    <form onSubmit={proceedToCheckout}>
                        <AddressInputs 
                            addressProps={address}
                            setAddressProp={handleAddressChange}
                        />
                        <button type="submit">Pay ${subtotal+5}</button>
                    </form>
                </div>
            </div>
        </section>
    )
}